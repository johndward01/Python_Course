
# REPL - Python Interactive Shell
# What is REPL? REPL is the language shell, the Python Interactive Shell.
# The REPL acronym is short for Read, Eval, Print and Loop.

# To start the Python language shell (the interactive shell), first open a terminal or command prompt.
# The Python interactive interpreter can be used to easily check Python commands.
# Type the command python without any parameter and hit ENTER.

# How to Quit the Python Shell
# If you started the Python interactive shell, you may be wondering how to exit it.
# There are several ways to quit the Python interactive shell (REPL).
#
# One way is by typing exit(). This is calling the function exit,
# which is why the brackets in exit() must be written.

